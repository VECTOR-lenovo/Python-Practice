def area_traingle(b, h):  # Defines a function to calculate the area of a triangle
    return b*h/2  # Returns the area using the formula (base * height) / 2

area = area_traingle(4, 8)  # Calls the function with base 4 and height 8
print("area of traingle is: " + str(area))  # Prints the area of the triangle